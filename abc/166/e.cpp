int main(void)
{
    ll n;

    cin >> n;

    vector<ll> a(n);
    map<ll, ll> d;

    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    for (int i = 0; i < n; i++)
    {
        d[i + 1 - a[i]]++;
    }

    ll ans = 0;

    for (int i = 0; i < n; i++)
    {
        ans += d[i + 1 + a[i]];
    }

    cout << ans << endl;
}
