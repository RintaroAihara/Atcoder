template <typename T>
bool chmax(T &a, const T &b)
{
    if (a < b)
    {
        a = b;
        return true;
    }
    return false;
}

template <typename T>
bool chmin(T &a, const T &b)
{
    if (a > b)
    {
        a = b;
        return true;
    }
    return false;
}

int main(void)
{
    ll n;
    cin >> n;

    string s;
    map<char, ll> m;

    for (int i = 0; i < n; i++)
    {
        cin >> s;
        m[s[0]]++;
    }

    char march[5] = {'M', 'A', 'R', 'C', 'H'};

    ll ans = 0;

    for (int i = 0; i < 5; i++)
    {
        for (int j = i + 1; j < 5; j++)
        {
            for (int k = j + 1; k < 5; k++)
            {
                ans += m[march[i]] * m[march[j]] * m[march[k]];
            }
        }
    }

    cout << ans << endl;
}